package cz.vutbr.fit.xproko26.pivis.parser;

import java.util.BitSet;
import org.antlr.v4.runtime.ANTLRErrorListener;
import org.antlr.v4.runtime.Parser;
import org.antlr.v4.runtime.RecognitionException;
import org.antlr.v4.runtime.Recognizer;
import org.antlr.v4.runtime.atn.ATNConfigSet;
import org.antlr.v4.runtime.dfa.DFA;
import org.antlr.v4.runtime.misc.ParseCancellationException;

/**
 * ErrorListener overrides syntaxError method to throw ParseCancellationException.
 * @author Dagmar Prokopova
 */
public class ErrorListener implements ANTLRErrorListener {
    
    //singleton instance of ErrorListener
    public static ErrorListener instance;
    
    /**
     * Method for accessing the singleton instance.
     * @return instance of TextParser class
     */
    public static ErrorListener getInstance() {
        if(instance == null) {
            instance = new ErrorListener();
        }
        return instance;
    }

    @Override
    public void syntaxError(Recognizer<?, ?> rcgnzr, Object o, int line, int charpos, String msg, RecognitionException re) throws ParseCancellationException {
        throw new ParseCancellationException(msg);
    }

    @Override
    public void reportAmbiguity(Parser parser, DFA dfa, int i, int i1, boolean bln, BitSet bitset, ATNConfigSet atncs) {
    }

    @Override
    public void reportAttemptingFullContext(Parser parser, DFA dfa, int i, int i1, BitSet bitset, ATNConfigSet atncs) {
    }

    @Override
    public void reportContextSensitivity(Parser parser, DFA dfa, int i, int i1, int i2, ATNConfigSet atncs) {
    }

}
